<p></p><h1>Fantasy WWE</h1><br>
<i>For 2-8 players, ages 13 and up</i>

<p>Fantasy WWE is a game of strategy, prediction, knowledge of the professional wrestling industry, and luck. Players compete against each other as Managers of a Stable of performers (Superstars, Divas, and Tag Teams) that earn points based on actual in-ring accomplishments on sports entertainment programming. Do you have what it takes to be the next Freddy Blassie, Bobby Heenan, or Paul Heyman?

<a name="organization"></a></p><h2><a name="organization">Organization</a></h2>
The Professional Wrestling Fantasy Alliance (PWFA) is comprised of individual Promotions of two to eight players. Each player may manage one Stable in a Promotion and is responsible for guiding his or her own Stable to victory. Active participation, trading of performers, and promo-cutting are encouraged.

<p>The game is administered by the Chairman of the PWFA. Where these rules are insufficient, the Chairman has the authority to issue binding rulings and to declare final decisions on all disputes among players.</p>

<p></p><p>[ <a href="#top">back to top</a> ]
</p><p></p><hr width="300" align="left">

<a name="season"><h2>Season</h2></a>

Before the Fantasy WWE game begins, the Chairman determines and announces the duration of the season. A season must begin the Monday after a pay-per-view event; a season must end with the conclusion of a pay-per-view event.

<p>The game is scored weekly, with each week of the season running from Monday through the completion of the airing of all original televised sports entertainment programming the following Sunday.

</p><p>[ <a href="#top">back to top</a> ]
</p><p></p><hr width="300" align="left">

<a name="stables"><h2>Stables</h2></a>

Each player signs and manages a Stable of 10 performers, attempting to anticipate which performers will earn the player the most points each week. Of the Stable's 10 performers, seven may earn points for the player in a given week, while the other three await their turn in the Locker Room.

<a name="class"><h3>Classification</h3></a>

WWE performers compete on four regular weekly television shows (Monday Night RAW, WWE Main Event, Friday Night SmackDown, and WWE Superstars), at pay-per-view events that are held approximately once per month, and infrequently on other special occasions.

<p>In Fantasy WWE, each individual in-ring male performer ("Superstar") is classified by tier based on whether the superstar is being "booked" in the Main Event (ME), Mid-Card (MC), or Under-Card (UC).

</p><p>In-ring female performers ("Divas") are classified simply as Divas (DI).

</p><p>A final classification, Tag Team (TT), is for teams of male Superstars -- whether well-established or formed <i>ad hoc</i> -- that compete in the tag team division; there are no subdivisions of the TT classification. Any two superstars can be paired to fill the TT Stable spot.

</p><p>Occasionally, a tag team may consist of more than two superstars. In such cases, the Chairman may decide to employ the "Freebird Rule," which provides that any two Superstars from the tag team can earn points for the tag team.

</p><p>Each player decides which of his 10 performers to place in seven active slots each week. The slots include: one reserved for performers classified as Main Event (ME), two for those in the Mid-Card (MC), one for those in the Under-Card (UC), one for a Diva (DI), one for a tag team (TT), and one slot that is unreserved and can be filled by a performer from any classification (FLEX). The three remaining performers under contract to the player remain in the Locker Room for the week and do not earn any points for the player.

</p><p>If a performer is injured, suspended, or otherwise inactive for at least 30 days, he or she will be considered "Inactive" and a corresponding notation will appear next to the performer's name. This designation serves only as a courtesy to the players. All "Inactive" performers are eligible for active and inactive Stable slots; may be signed, released, or traded; and may be drafted (see below).

</p><p>All performers not signed by a player are considered Free Agents and subsequently may be signed to a Stable pursuant to the Transactions rules outlined below.

</p><p>When a new performer is added to the WWE roster, the Chairman shall immediately classify the new performer and the performer becomes a restricted free agent, effective the following week (see Transactions below).

<a name="draft"></a></p><h3><a name="draft">Draft</a></h3>

Stables are initially stocked via a draft similar to the draft held by most fantasy sports leagues. The draft consists of 10 rounds in which each player has the opportunity to select one available performer per round until all 10 of the player's Stable spots are filled. The player that selects last in each round of the draft will select first in the succeeding round. Initially, the order of selection for the first round of the draft is randomly assigned; see "Play it again!" below for subsequent seasons.

<p>Performers on the Inactive list are eligible to be drafted.

<a name="reclass"></a></p><h3><a name="reclass">Reclassification</a></h3>

After each pay-per-view event, the Chairman may choose to reclassify Superstars and Divas to reflect the way they are currently being booked (i.e. move a UC superstar up to the MC tier). Without exception, world and women's champions and number-one contenders for those titles will be classified in the Main Event; mid-card champions and number-one contenders for those titles will be classified in the Mid-Card. As a courtesy to the other players, the Chairman should notify the entire PWFA as soon as he or she begins to consider reclassifying performers so players can begin to assess the transactions they may need to make.

<p>The Chairman shall not abuse this power to the benefit or detriment of any player. Reclassifications can be vetoed by a majority vote of all players.

</p><p>[ <a href="#top">back to top</a> ]
</p><p></p><hr width="300" align="left">

<a name="transact"><h2>Transactions</h2></a>

All Stables are locked for the week Monday at 8 p.m. Eastern U.S. time. The Stable in place at that time is the Stable the player must use for the duration of that week.

<p></p><p>Players may drop performers from their Stable, add an unrestricted free agent to their Stable, place a claim on a restricted free agent, or trade performers with another player at any time. However, the results of those transactions are not in effect until the week following the transaction.

<a name="ufa"></a></p><h3><a name="ufa">Unrestricted free agents</a></h3>

All performers that are not chosen in the draft at the start of the season become unrestricted free agents. Unrestricted free agents can be added by any player to his or her Stable on a first-come, first-serve basis. A player cannot have more than one performer in any Stable spot, so the player must vacate the appropriate Stable spot by dropping the performer in that Stable spot before he or she can add another in that Stable spot.

<a name="rfa"><h3>Restricted free agents</h3></a>

When a performer is dropped from a Stable, the performer becomes a restricted free agent.

<p></p><p>The restricted free agent is ineligible for use by any player for the week after the performer is dropped. During that week, all interested players may claim the restricted free agent in an attempt to add the restricted free agent to his or her Stable.

</p><p>At the conclusion of the week, the Chairman shall award the restricted free agent to the player with the worst overall winning percentage that placed a claim on the restricted free agent. If no claim is made on the restricted free agent, the performer becomes an unrestricted free agent and may be added by any player on a first-come, first-serve basis.

<a name="trades"></a></p><h3><a name="trades">Trades</a></h3>

Players may trade performers between and among each other. Keep in mind that when a trade is complete, only one performer may occupy each slot in a player's Stable.

<p></p><p>Trading shall only be done with the intention of improving one's own team. To avoid foul play, the Chairman shall establish a trade deadline several weeks before the end of the season after which no further trades may take place. This is to prevent a player that is out of contention for the Fantasy WWE Championship from intentionally trading valuable performers to bolster another player's chances of victory.

</p><p>[ <a href="#top">back to top</a> ]
</p><p></p><hr width="300" align="left">

<a name="scoring"><h2>Scoring</h2></a>

Players accrue points throughout each week of the season based on match outcomes. The player that earns the most points in a week is awarded a win for that week. If two or more players earn the same number of points in a week, those players are awarded a tie for that week.

<a name="singles"><h3>Singles matches</h3></a>

In a singles match, a victory by pinfall, submission, knockout, referee stoppage, or fulfillment of a special stipulation in the match (i.e. escaping the steel cage) is worth 2 points. A victory in a battle royal (including the Royal Rumble, but see below for Royal Rumble bonus points) is worth 2 points. A victory by disqualification, count-out, or forfeit is worth 1 point.

<p></p><p>Point values are doubled for victories at all pay-per-view events unless otherwise specified.

</p><p>In a singles match involving more than two competitors (i.e. Triple Threat Match), the victorious performer earns one point for each competitor in the match (i.e. three points for winning a Triple Threat Match).

<a name="tag"></a></p><h3><a name="tag">Tag Team matches</a></h3>

A tag team earns points as a unit (i.e. 2 points for a pinfall victory by the tag team); the individual members of the tag team earn half the point value that the team earns (i.e. 1 point for a pinfall victory by the tag team). Example: If the Usos win by pinfall, the player owning the Usos as his or her Tag Team (TT) earns two points; the players owning the members of the Usos, Jimmy Uso and Jey Uso, earn one point each.
	
<p></p><p>In matches that involve more than two competitors per team (i.e. six-man or eight-man tag teams, excluding elimination matches including Survivor Series matches), each tag team on the winning team earns 2 points and each individual member on the winning team earns 1 point.

<a name="multiple"></a></p><h3><a name="multiple">Multiple-fall matches</a></h3>

In a Two-out-of-Three-Falls Match, the winning performer earns 3 points; the losing performer earns 0 points, even if he or she did score one fall.

<p></p><p>In a Survivor Series Match, only the surviving members of the winning team earn points. Each surviving member earns points for each fall he/she personally scored (i.e. 2 points for a pinfall) plus a 2-point survival bonus. These points are not doubled.

</p><p>An Elimination Chamber Match is scored similarly to a Survivor Series Match: only the surviving performers earn points, the winner is awarded points for each fall he or she personally scored, and the winner receives a 2-point bonus for winning. These points are not doubled.

<a name="bonuses"></a></p><h3><a name="bonuses">Bonuses</a></h3>

A performer earns an additional bonus of 5 points for winning a world (WWE, World Heavyweight, or WWE Tag Team) title. He or she earns 3 points for winning a mid-card (Intercontinental or United States) title or the Divas' Championship.

<p></p><p>The Royal Rumble winner earns a bonus of 4 points (for a total score of 8 points: 2 for winning a battle royal, times 2 for winning a pay-per-view match, plus the four-point Royal Rumble bonus).

</p><p>All bonuses are added after victory points are doubled, if applicable.

<a name="othscoring"></a></p><h3><a name="othscoring">Other scoring rules</a></h3>

Victories over performers not listed on the official WWE roster (i.e. "local competitors"), including WWE NXT performers, are worth half the points of a normal victory (i.e. 1 point for a pinfall/submission win, 0.5 points for a disqualification/count-out win).

<p></p><p>If a match is ruled a no contest, no points are awarded.

</p><p>The source for all match results is WWE.com. A player may petition the Chairman to overrule the official results posted on WWE.com if he or she feels the results are incorrect. If a majority of players supports the petition, the results will be amended.

</p><p>The Chairman will publish the results of each televised event and an updated scoreboard as soon as possible after the conclusion of each event. The Chairman will announce each week's winner and provide an updated standings table at the conclusion of each week.

</p><p>[ <a href="#top">back to top</a> ]
</p><p></p><hr width="300" align="left">

<a name="winning"><h2>Winning the game</h2></a>

At the end of the season, the player with the highest winning percentage is crowned Fantasy WWE Champion.

<p></p><p>If more than one player is tied for the highest winning percentage, the player that earned the most overall points throughout the season is awarded the Championship.

</p><p>If the players tied for the highest winning percentage also have the same number of overall points, they may fight in a steel cage to determine the Champion, or they may choose to be declared Co-Champions.

